// Get a random subset of tips
export function getRandomTips(count = 3) {
  const shuffled = [...financialTips].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// Get a tip by index (for consistent daily tip)
export function getDailyTip() {
  const dayOfYear = Math.floor((Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86400000);
  return financialTips[dayOfYear % financialTips.length];
}

// Get tips relevant to the user's financial situation
export function getContextualTips(state, count = 2) {
  const tips = [];

  // Debt-related
  if (state.debts?.length > 10) {
    tips.push("With many debts, consider the snowball method — pay off the smallest balance first for quick wins, then roll that payment into the next debt.");
  }
  if (state.debts?.some(d => d.totalDebt > 100000)) {
    tips.push("For large debts, even small extra payments make a big difference over time due to compound interest working in reverse.");
  }

  // Income vs expenses
  const income = state.income?.reduce((s, i) => s + (Number(i.amount) || 0), 0) || 0;
  const expenses = state.monthlyExpenses?.reduce((s, e) => s + (Number(e.amount) || 0), 0) || 0;
  if (expenses > income * 0.8) {
    tips.push("Your expenses are using over 80% of your income. Look for your top 3 variable expenses and see if you can reduce them by 10%.");
  }
  if (income > 0 && expenses < income * 0.5) {
    tips.push("Great job keeping expenses low! Consider putting the surplus into a high-yield savings account or investment fund.");
  }

  // Property
  if (state.propertyExpenses?.length > 0) {
    const completed = state.propertyExpenses.filter(p => p.completed).length;
    const total = state.propertyExpenses.length;
    if (completed / total < 0.5) {
      tips.push("Focus on completing high-impact property improvements first — kitchen and bathroom updates typically offer the best ROI.");
    }
  }

  // Pad with random tips if needed
  while (tips.length < count) {
    const random = financialTips[Math.floor(Math.random() * financialTips.length)];
    if (!tips.includes(random)) tips.push(random);
  }

  return tips.slice(0, count);
}
